from os import system, name
# ---------------------------------------------------------------------------------------------------------------------------------------
# This class is a simple function that will reset the terminal for the user.
# ---------------------------------------------↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓---------------------------------------------
class Clear:
    def clear(self):
        # ---------------------------------------------------------------------------------------
        # if the user is running windows this line will run.
        # -----------------------------↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓-----------------------------
        if name == 'nt':
            _ = system('cls')
        # ---------------------------------------------------------------------------------------
        # if the user is using mac or linux this line will run (here, os.name is 'posix')
        # -----------------------------↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓↓-----------------------------
        else:
            _ = system('clear')

